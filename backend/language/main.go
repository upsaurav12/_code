// Function to find and print common keys and their values from both maps
func findMatchingSyscalls(map1, map2 map[int]string) {
	fmt.Println("Syscalls with Matching Key-Value Pairs:")
	found := false
	for key, val1 := range map1 {
		if val2, exists := map2[key]; exists && val1 == val2 {
			fmt.Printf("  - Syscall: %s | Value: %d\n", key, val1)
			found = true
		}
	}
	if !found {
		fmt.Println("  No matching syscalls found.")
	}
}

